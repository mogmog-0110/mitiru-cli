// Package build generates a CMakeLists.txt for a MitiruEngine user project
// and drives cmake configure/build through the local Visual Studio toolchain.
package build

import (
	"bufio"
	"bytes"
	"fmt"
	"io"
	"os"
	"os/exec"
	"path/filepath"
	"runtime"
	"strings"
	"text/template"

	"github.com/mogmog-0110/mitiru-cli/internal/engine"
)

// Options drives both CMakeLists.txt generation and the cmake invocation.
type Options struct {
	// ProjectRoot is the absolute path of the directory containing mitiru.toml.
	ProjectRoot string

	// ProjectName is project.name from mitiru.toml. Used as both the CMake
	// project() name and the SHARED library target name.
	ProjectName string

	// EngineRoot is the absolute path of the engine source — the directory
	// holding the engine's top-level CMakeLists.txt.
	EngineRoot string

	// Config is "Debug" or "Release".
	Config string

	// Generator is the explicit CMake generator override (e.g. "NMake Makefiles",
	// "Visual Studio 17 2022"). Empty means use the default for the detected
	// vcvars toolchain.
	Generator string

	// Stdout receives progress and cmake output.
	Stdout io.Writer

	// Stderr receives error output.
	Stderr io.Writer
}

type templateData struct {
	ProjectName string
	TargetName  string
	EngineRoot  string
	ProjectRoot string
	MainCppAbs  string
	HostMainAbs string
}

// CMake template — emits two targets per project:
//
//   <TargetName>      SHARED library, the user's game DLL.
//   mitiru_host       Universal launcher exe (engine reference impl,
//                     compiled from ${MITIRU_ENGINE_ROOT}/examples/mitiru_host/main.cpp).
//
// Layout in <projectRoot>/build/out/<Config>/:
//   mitiru_host.exe
//   libcef.dll, *.pak, locales/, MitiruCefHelper.exe   (deployed via mitiru_add_cef_game)
//   <TargetName>/
//     <TargetName>.dll
//     assets/
//       scene.html
//       mitiru_runtime/mitiru_cef_state.js
//
// Launch:  mitiru_host.exe <TargetName>/<TargetName>.dll
// The host derives the CEF start URL from the DLL path automatically.
const cmakeTemplate = `# Generated by mitiru-cli. Do not edit by hand — re-run 'mitiru build'.
cmake_minimum_required(VERSION 3.20)
project({{.ProjectName}} LANGUAGES CXX)

set(CMAKE_CXX_STANDARD 20)
set(CMAKE_CXX_STANDARD_REQUIRED ON)
set(CMAKE_CXX_EXTENSIONS OFF)

# Engine source (cached by mitiru-cli)
set(MITIRU_ENGINE_ROOT "{{.EngineRoot}}")
set(MITIRU_HEADER_ONLY ON)
add_subdirectory("${MITIRU_ENGINE_ROOT}" mitiru-engine)

include("${MITIRU_ENGINE_ROOT}/cmake/MitiruCef.cmake")

set(MITIRU_PROJECT_ROOT "{{.ProjectRoot}}")

# ── Game DLL (the user's project) ─────────────────────────────────
add_library({{.TargetName}} SHARED "{{.MainCppAbs}}")
target_link_libraries({{.TargetName}} PRIVATE Mitiru::mitiru)
if(MSVC)
    target_compile_options({{.TargetName}} PRIVATE /bigobj)
endif()

# ── Host launcher (compiled from engine reference impl) ───────────
add_executable(mitiru_host "{{.HostMainAbs}}")
target_link_libraries(mitiru_host PRIVATE Mitiru::mitiru)
if(MSVC)
    target_compile_options(mitiru_host PRIVATE /bigobj)
endif()
if(WIN32)
    set_target_properties(mitiru_host PROPERTIES WIN32_EXECUTABLE OFF)
endif()
mitiru_add_cef_game(mitiru_host)

# ── Deploy the game DLL into <host_dir>/<TargetName>/ ─────────────
# Putting the DLL next to its assets keeps the host's cwd anchor stable
# (cwd = mitiru_host dir → relative path "<TargetName>/assets/scene.html").
set(_game_runtime_dir "$<TARGET_FILE_DIR:mitiru_host>/{{.TargetName}}")
set_target_properties({{.TargetName}} PROPERTIES
    RUNTIME_OUTPUT_DIRECTORY "${_game_runtime_dir}"
    LIBRARY_OUTPUT_DIRECTORY "${_game_runtime_dir}")

# Copy user assets/ into the deploy dir on every build.
if(EXISTS "${MITIRU_PROJECT_ROOT}/assets")
    add_custom_command(TARGET {{.TargetName}} POST_BUILD
        COMMAND ${CMAKE_COMMAND} -E copy_directory
            "${MITIRU_PROJECT_ROOT}/assets"
            "${_game_runtime_dir}/assets"
        COMMENT "mitiru-cli: copying assets/ next to {{.TargetName}}.dll")
endif()

# Drop the JS bridge runtime (mitiru_cef_state.js) into the deploy dir so
# <script src="mitiru_runtime/..."> resolves regardless of where the user
# put their HTML.
if(EXISTS "${MITIRU_ENGINE_ROOT}/web/mitiru_runtime")
    add_custom_command(TARGET {{.TargetName}} POST_BUILD
        COMMAND ${CMAKE_COMMAND} -E copy_directory
            "${MITIRU_ENGINE_ROOT}/web/mitiru_runtime"
            "${_game_runtime_dir}/assets/mitiru_runtime"
        COMMENT "mitiru-cli: copying mitiru_runtime/* into deployed assets/")
endif()

# Make sure mitiru_host is built (and its CEF runtime deployed) before
# the DLL POST_BUILD copies fire — the generator expression above resolves
# at build time and needs the host to exist.
add_dependencies({{.TargetName}} mitiru_host)
`

// BuildDirs computes the standard layout of build artefacts for projectRoot:
//
//	<projectRoot>/build/cmake/      ← generated CMakeLists.txt source tree
//	<projectRoot>/build/out/        ← cmake -B output directory
func BuildDirs(projectRoot string) (cmakeSrcDir, cmakeOutDir string) {
	cmakeSrcDir = filepath.Join(projectRoot, "build", "cmake")
	cmakeOutDir = filepath.Join(projectRoot, "build", "out")
	return
}

// Configure generates the CMakeLists.txt at <projectRoot>/build/cmake/ and
// returns the source / output directories that should be passed to cmake.
func Configure(opts Options) (cmakeSrcDir, cmakeOutDir string, err error) {
	cmakeSrcDir, cmakeOutDir = BuildDirs(opts.ProjectRoot)

	if err := os.MkdirAll(cmakeSrcDir, 0o755); err != nil {
		return "", "", fmt.Errorf("create build cmake dir: %w", err)
	}
	if err := os.MkdirAll(cmakeOutDir, 0o755); err != nil {
		return "", "", fmt.Errorf("create build out dir: %w", err)
	}

	mainCpp := filepath.Join(opts.ProjectRoot, "src", "main.cpp")
	if _, statErr := os.Stat(mainCpp); statErr != nil {
		return "", "", fmt.Errorf("expected source file %s: %w", mainCpp, statErr)
	}

	hostMain := filepath.Join(opts.EngineRoot, "examples", "mitiru_host", "main.cpp")
	if _, statErr := os.Stat(hostMain); statErr != nil {
		return "", "", fmt.Errorf(
			"engine reference host source missing: %s\n"+
				"  the cached engine tree looks incomplete or pre-v0.2.0;\n"+
				"  set MITIRU_ENGINE_ROOT to a v0.2.0+ checkout or re-run `mitiru build`\n"+
				"  with MITIRU_CACHE_DIR cleared",
			hostMain)
	}

	data := templateData{
		ProjectName: opts.ProjectName,
		TargetName:  sanitiseTargetName(opts.ProjectName),
		EngineRoot:  toCMakePath(opts.EngineRoot),
		ProjectRoot: toCMakePath(opts.ProjectRoot),
		MainCppAbs:  toCMakePath(mainCpp),
		HostMainAbs: toCMakePath(hostMain),
	}

	tmpl, err := template.New("CMakeLists").
		Option("missingkey=error").
		Parse(cmakeTemplate)
	if err != nil {
		return "", "", fmt.Errorf("parse cmake template: %w", err)
	}

	out, err := os.Create(filepath.Join(cmakeSrcDir, "CMakeLists.txt"))
	if err != nil {
		return "", "", fmt.Errorf("create generated CMakeLists.txt: %w", err)
	}
	defer out.Close()

	if err := tmpl.Execute(out, data); err != nil {
		return "", "", fmt.Errorf("render cmake template: %w", err)
	}
	return cmakeSrcDir, cmakeOutDir, nil
}

// Artifacts is what the build pipeline produces — both the user's DLL and
// the host exe that drives it. `mitiru run` invokes `HostExePath DllPath`.
type Artifacts struct {
	// DeployDir is the directory containing mitiru_host.exe and the
	// per-project subdirectory holding <TargetName>.dll + assets/. The
	// host expects to cwd here before loading the DLL.
	DeployDir string

	// HostExePath is the absolute path of mitiru_host.exe.
	HostExePath string

	// DllPath is the absolute path of the user's <TargetName>.dll.
	DllPath string

	// DllRel is DllPath expressed relative to DeployDir, suitable for
	// passing to mitiru_host as `mitiru_host.exe <DllRel>`.
	DllRel string
}

// Run executes the full configure + build pipeline. Returns Artifacts on
// success — both the host launcher and the user's game DLL.
func Run(opts Options) (*Artifacts, error) {
	if runtime.GOOS != "windows" {
		return nil, fmt.Errorf("mitiru build is currently Windows-only (running on %s)",
			runtime.GOOS)
	}

	if opts.Stdout == nil {
		opts.Stdout = os.Stdout
	}
	if opts.Stderr == nil {
		opts.Stderr = os.Stderr
	}
	if opts.Config == "" {
		opts.Config = "Debug"
	}

	vcvars, err := FindVcvars64()
	if err != nil {
		return nil, err
	}
	generator := opts.Generator
	if generator == "" {
		generator = generatorForVcvars(vcvars)
	}

	cmakeSrcDir, cmakeOutDir, err := Configure(opts)
	if err != nil {
		return nil, err
	}

	// A previously-configured build dir pins its generator in CMakeCache.txt.
	// Switching generators (e.g. NMake → Ninja) makes cmake abort with a
	// "generator mismatch" error, so wipe the output tree when the cached
	// generator differs from the one we're about to use. The generated source
	// tree (cmakeSrcDir) is untouched — only the cmake -B output is rebuilt.
	if mismatch, cached := generatorMismatch(cmakeOutDir, generator); mismatch {
		fmt.Fprintf(opts.Stdout,
			"Generator changed (%s -> %s); clearing %s for a clean reconfigure...\n",
			cached, generator, cmakeOutDir)
		if rmErr := os.RemoveAll(cmakeOutDir); rmErr != nil {
			return nil, fmt.Errorf("clear stale build dir %s: %w", cmakeOutDir, rmErr)
		}
		if mkErr := os.MkdirAll(cmakeOutDir, 0o755); mkErr != nil {
			return nil, fmt.Errorf("recreate build dir %s: %w", cmakeOutDir, mkErr)
		}
	}

	if err := engine.EnsureCEF(opts.EngineRoot, opts.Stdout); err != nil {
		return nil, fmt.Errorf("CEF setup failed: %w", err)
	}

	fmt.Fprintf(opts.Stdout, "Configuring %s (%s)...\n", opts.ProjectName, opts.Config)
	if err := runCMakeConfigure(vcvars, generator, cmakeSrcDir, cmakeOutDir, opts); err != nil {
		return nil, err
	}

	fmt.Fprintf(opts.Stdout, "Building %s (%s)...\n", opts.ProjectName, opts.Config)
	if err := runCMakeBuild(vcvars, cmakeOutDir, opts); err != nil {
		return nil, err
	}

	targetName := sanitiseTargetName(opts.ProjectName)

	// In dry-run mode nothing was actually built; return placeholder paths
	// so callers can still log the planned launch command.
	if os.Getenv("MITIRU_DRY_RUN") == "1" {
		deploy := filepath.Join(cmakeOutDir, opts.Config)
		return &Artifacts{
			DeployDir:   deploy,
			HostExePath: filepath.Join(deploy, "mitiru_host.exe"),
			DllPath:     filepath.Join(deploy, targetName, targetName+".dll"),
			DllRel:      filepath.Join(targetName, targetName+".dll"),
		}, nil
	}

	// Locate mitiru_host.exe — its parent directory is the deploy dir.
	hostCandidates := []string{
		filepath.Join(cmakeOutDir, opts.Config, "mitiru_host.exe"),
		filepath.Join(cmakeOutDir, "mitiru_host.exe"),
	}
	hostExe := ""
	for _, c := range hostCandidates {
		if _, err := os.Stat(c); err == nil {
			hostExe = c
			break
		}
	}
	if hostExe == "" {
		return nil, fmt.Errorf(
			"build succeeded but mitiru_host.exe was not found under %s\n"+
				"  expected one of:\n    %s",
			cmakeOutDir, strings.Join(hostCandidates, "\n    "))
	}

	deployDir := filepath.Dir(hostExe)
	dllPath := filepath.Join(deployDir, targetName, targetName+".dll")
	if _, err := os.Stat(dllPath); err != nil {
		return nil, fmt.Errorf(
			"build succeeded but %s.dll was not found at %s: %w",
			targetName, dllPath, err)
	}

	return &Artifacts{
		DeployDir:   deployDir,
		HostExePath: hostExe,
		DllPath:     dllPath,
		DllRel:      filepath.Join(targetName, targetName+".dll"),
	}, nil
}

func runCMakeConfigure(vcvars, generator, srcDir, outDir string, opts Options) error {
	// Multi-config generators (Visual Studio) accept and require -A; single
	// config generators (NMake, Ninja) reject it AND need an explicit
	// CMAKE_BUILD_TYPE.
	var archAndType string
	if isMultiConfigGenerator(generator) {
		archAndType = "-A x64"
	} else {
		archAndType = "-DCMAKE_BUILD_TYPE=" + opts.Config
	}

	script := fmt.Sprintf(
		"%scmake -S \"%s\" -B \"%s\" -G \"%s\" %s\r\n",
		vcvarsPrelude(vcvars), srcDir, outDir, generator, archAndType)

	// CMake's configure output is pure diagnostic noise on success — feature
	// detection lines like "Jolt not found", "Tracy not found" that read as
	// errors to a first-time user and blow the first-touch output budget.
	// Capture it and surface it only if configure actually fails. Opt back in
	// with MITIRU_VERBOSE=1 (dry-run already routes through its own printer).
	if os.Getenv("MITIRU_DRY_RUN") == "1" || os.Getenv("MITIRU_VERBOSE") == "1" {
		return runBatchScript("mitiru_configure", script, opts)
	}
	var buf bytes.Buffer
	quiet := opts
	quiet.Stdout = &buf
	quiet.Stderr = &buf
	if err := runBatchScript("mitiru_configure", script, quiet); err != nil {
		if buf.Len() > 0 {
			fmt.Fprint(opts.Stderr, buf.String())
		}
		return err
	}
	return nil
}

// vcvarsPrelude emits the boilerplate every cmake-driving batch script needs:
// puts the VS Installer dir on PATH (so vcvars64.bat can find vswhere.exe
// without printing "is not recognized" noise), then activates vcvars64.bat.
func vcvarsPrelude(vcvars string) string {
	return fmt.Sprintf(
		"@echo off\r\n"+
			"set \"PATH=C:\\Program Files (x86)\\Microsoft Visual Studio\\Installer;%%PATH%%\"\r\n"+
			"call \"%s\" >NUL\r\n"+
			"if errorlevel 1 exit /b %%errorlevel%%\r\n",
		vcvars)
}

func isMultiConfigGenerator(generator string) bool {
	return strings.HasPrefix(generator, "Visual Studio") ||
		generator == "Ninja Multi-Config" ||
		generator == "Xcode"
}

// generatorForVcvars maps a vcvars64.bat path to the CMake generator name for
// that toolchain. Ninja is the default everywhere on Windows:
//   - Like NMake it consumes the LIB / INCLUDE env vars vcvars64.bat sets
//     directly, so it avoids the MSBuild/SDK resolution problem the VS
//     multi-config generator hits (LNK1104 ucrtd.lib on machines where only an
//     older Windows SDK is installed).
//   - Unlike NMake it does NOT choke on the drive-letter colon in Windows paths
//     inside CMakeFiles\<target>.dir\compiler_depend.make — NMake aborts there
//     with "fatal error U1033: 構文エラー : 予期しない ':'", Ninja does not.
//   - VS bundles ninja.exe and vcvars64.bat puts it on PATH, so no extra install.
// Users who want NMake or a .sln can opt-in with
// `mitiru build --generator "NMake Makefiles"` / `--generator "Visual Studio 17 2022"`.
func generatorForVcvars(vcvars string) string {
	_ = vcvars
	return "Ninja"
}

// generatorMismatch reports whether outDir holds a CMakeCache.txt whose
// pinned CMAKE_GENERATOR differs from want. Returns (false, "") when there is
// no cache yet (a fresh build dir is never a mismatch) or when the cache is
// unreadable/has no generator line (let cmake itself decide in that case).
func generatorMismatch(outDir, want string) (bool, string) {
	f, err := os.Open(filepath.Join(outDir, "CMakeCache.txt"))
	if err != nil {
		return false, ""
	}
	defer f.Close()

	const prefix = "CMAKE_GENERATOR:INTERNAL="
	scanner := bufio.NewScanner(f)
	for scanner.Scan() {
		line := strings.TrimSpace(scanner.Text())
		if strings.HasPrefix(line, prefix) {
			cached := strings.TrimSpace(strings.TrimPrefix(line, prefix))
			return cached != want, cached
		}
	}
	return false, ""
}

func runCMakeBuild(vcvars, outDir string, opts Options) error {
	script := fmt.Sprintf(
		"%scmake --build \"%s\" --config %s\r\n",
		vcvarsPrelude(vcvars), outDir, opts.Config)
	return runBatchScript("mitiru_build", script, opts)
}

// runBatchScript materialises the given script as a .bat under the OS temp
// dir and invokes it via `cmd /c`. Writing a file (rather than passing the
// pipeline inline to cmd /c) sidesteps Windows command-line quoting bugs
// when paths contain spaces.
func runBatchScript(prefix, script string, opts Options) error {
	if v := os.Getenv("MITIRU_DRY_RUN"); v == "1" {
		fmt.Fprintf(opts.Stdout, "[dry-run] would execute %s script:\n%s\n", prefix, script)
		return nil
	}

	tmp, err := os.CreateTemp("", prefix+"-*.bat")
	if err != nil {
		return fmt.Errorf("create temp batch: %w", err)
	}
	scriptPath := tmp.Name()
	defer func() { _ = os.Remove(scriptPath) }()

	if _, err := tmp.WriteString(script); err != nil {
		_ = tmp.Close()
		return fmt.Errorf("write temp batch: %w", err)
	}
	if err := tmp.Close(); err != nil {
		return fmt.Errorf("close temp batch: %w", err)
	}

	cmd := exec.Command("cmd", "/c", scriptPath)
	cmd.Stdout = opts.Stdout
	cmd.Stderr = opts.Stderr
	cmd.Dir = opts.ProjectRoot
	if err := cmd.Run(); err != nil {
		return fmt.Errorf("command failed (%w): %s", err, scriptPath)
	}
	return nil
}

// FindVcvars64 hunts for vcvars64.bat using the same candidate set as
// `mitiru doctor`. Kept here (rather than importing internal/commands) to
// avoid a back-reference from a leaf package.
func FindVcvars64() (string, error) {
	candidates := []string{
		`C:\Program Files\Microsoft Visual Studio\18\Community\VC\Auxiliary\Build\vcvars64.bat`,
		`C:\Program Files\Microsoft Visual Studio\2022\Community\VC\Auxiliary\Build\vcvars64.bat`,
		`C:\Program Files\Microsoft Visual Studio\2022\Professional\VC\Auxiliary\Build\vcvars64.bat`,
		`C:\Program Files\Microsoft Visual Studio\2022\Enterprise\VC\Auxiliary\Build\vcvars64.bat`,
		`C:\Program Files\Microsoft Visual Studio\2022\BuildTools\VC\Auxiliary\Build\vcvars64.bat`,
	}
	for _, p := range candidates {
		if _, err := os.Stat(p); err == nil {
			return p, nil
		}
	}
	matches, _ := filepath.Glob(
		`C:\Program Files\Microsoft Visual Studio\*\*\VC\Auxiliary\Build\vcvars64.bat`)
	if len(matches) > 0 {
		return matches[0], nil
	}
	return "", fmt.Errorf("vcvars64.bat not found; install Visual Studio 2022 Build Tools (C++ workload) and re-run 'mitiru doctor'")
}

// toCMakePath rewrites a Windows path into the forward-slash form CMake
// always accepts and which avoids backslash-escape headaches in generated
// scripts.
func toCMakePath(p string) string {
	return filepath.ToSlash(p)
}

// sanitiseTargetName turns user-facing project names into CMake-safe target
// identifiers. Replaces anything that isn't [A-Za-z0-9_] with '_'.
func sanitiseTargetName(name string) string {
	out := make([]rune, 0, len(name))
	for _, r := range name {
		switch {
		case r >= 'A' && r <= 'Z',
			r >= 'a' && r <= 'z',
			r >= '0' && r <= '9',
			r == '_':
			out = append(out, r)
		default:
			out = append(out, '_')
		}
	}
	s := string(out)
	if s == "" || (s[0] >= '0' && s[0] <= '9') {
		s = "p_" + s
	}
	return s
}

