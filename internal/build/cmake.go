// toCMakePath は Windows path を、CMake が常に受け付ける forward-slash 形式に
// 書き換える。生成 script 内での backslash-escape の頭痛も避けられる。
func toCMakePath(p string) string {
	return filepath.ToSlash(p)
}

// sanitiseTargetName は user 向けの project 名を CMake-safe な target
// identifier に変換する。[A-Za-z0-9_] 以外を '_' に置き換える。
func sanitiseTargetName(name string) string {
	out := make([]rune, 0, len(name))
	for _, r := range name {
		switch {
		case r >= 'A' && r <= 'Z',
			r >= 'a' && r <= 'z',
			r >= '0' && r <= '9',
			r == '_':
			out = append(out, r)
		default:
			out = append(out, '_')
		}
	}
	s := string(out)
	if s == "" || (s[0] >= '0' && s[0] <= '9') {
		s = "p_" + s
	}
	return s
}

